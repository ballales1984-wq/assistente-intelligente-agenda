def detect_language_from_path(path):
    """Detect language from URL path"""
    if '/en' in path:
        return 'en'
    elif '/es' in path:
        return 'es'
    elif '/zh' in path:
        return 'zh'
    elif '/ru' in path:
        return 'ru'
    elif '/hi' in path:
        return 'hi'
    elif '/ar' in path:
        return 'ar'
    else:
        return 'it'

